package efeito_magnetico;

public class PontoMouse {
	public int x, y;

	public PontoMouse(int x, int y) {
		this.x = x;
		this.y = y;
	}

	public PontoMouse() {
	
	}
	
	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
	
	
}
